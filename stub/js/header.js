function signIn(){
    SignUp.style.display = "none";
    CreatePost.style.display = "none";
    SignIn.style.display = "block";

}

function close_signIn(){

    SignIn.style.display = "none";

}

function signUp(){
    SignIn.style.display = "none";
    CreatePost.style.display = "none";
    SignUp.style.display = "block";

}
function close_signUp(){

    SignUp.style.display = "none";

}